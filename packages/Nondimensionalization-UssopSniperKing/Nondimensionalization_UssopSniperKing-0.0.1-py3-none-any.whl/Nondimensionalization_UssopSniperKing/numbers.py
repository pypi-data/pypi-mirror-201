
def Reynolds(velocity, length, viscosity):
	"""Compute the Reynolds number

	Parameters
	----------
	velocity : float
 		velocity of the fluid [m s^-1]
	length : float
  		characteristic length of the system [m]
  	viscosity : float
  		cinematic viscosity of the fluid [m^2 s^-1]
   	
    """
	return (velocity * length)/viscosity
	

