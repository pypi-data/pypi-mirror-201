@name("gen.character.kitsch2")
def run(c, s, r):
    r(f"    Firstname: {c.words.firstname}")
    r(f"     Lastname: {c.words.lastname}")
    r(f"       Gender: {c.words.gender.set('gender')}")
    r()
    r(f"         Body: {c.words.body}")
    r(f"       Weight: {c.words.weight}")
    r(f"    Eye color: {c.words.eye_color}")
    r(f"   Hair color: {c.words.hair.color}")
    r(f"         Nose: {c.words.nose}")
    r()
    r(f"          Eye: {c.words.eye}")
    r(f"     Eyebrows: {c.words.eyebrows}")
    r(f"         Face: {c.words.face}")
    r(f" Face - cheek: {c.words.face.cheek}")
    r(f"  Face - chin: {c.words.face.chin}")
    r(f"  Facial hair: {c.words.face.facial_hair}", c.gender == "male")
    r(f" Hair - style: {c.words.hair.style}")
    r(f"   Hair - ext: {c.words.hair.style.ext}")
    r(f"   Skin color: {c.words.skin.color}")
