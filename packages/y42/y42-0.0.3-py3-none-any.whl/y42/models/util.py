from pydantic import BaseModel, Field


class GoogleCloudSecret(BaseModel):
    type_: str = Field(alias="type")
    project_id: str
    private_key_id: str
    private_key: str
    client_email: str
    client_id: str
    auth_uri: str
    token_uri: str
    auth_provider_x509_cert_url: str
    client_x509_cert_url: str
